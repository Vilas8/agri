-- AgriPredict MySQL Database Schema
-- Run this script in MySQL Workbench to create the database

-- Create database
CREATE DATABASE IF NOT EXISTS agri_predict;
USE agri_predict;

-- Table to store commodity prices
CREATE TABLE IF NOT EXISTS commodity_prices (
    id INT AUTO_INCREMENT PRIMARY KEY,
    date DATE NOT NULL,
    commodity VARCHAR(50) NOT NULL,
    price DECIMAL(10, 2) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UNIQUE KEY unique_commodity_date (date, commodity),
    INDEX idx_date (date),
    INDEX idx_commodity (commodity)
);

